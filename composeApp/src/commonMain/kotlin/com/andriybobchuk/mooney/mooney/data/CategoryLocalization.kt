/**
 * Returns the localized display title for the given seed transaction
 * [Category], or the user's custom title if it has been renamed.
 */
@Composable
fun localizedCategoryTitle(category: Category): String {
    val resId = CategoryLocalization.transactionTitleResIds[category.id] ?: return category.title
    val original = CategoryLocalization.transactionOriginalTitles[category.id]
    return if (category.title == original) stringResource(resId) else category.title
}

/**
 * Returns the localized display title for the given seed asset category,
 * or the user's custom title if it has been renamed.
 */
@Composable
fun localizedAssetCategoryTitle(category: AssetCategoryEntity): String {
    val resId = CategoryLocalization.assetTitleResIds[category.id] ?: return category.title
    val original = CategoryLocalization.assetOriginalTitles[category.id]
    return if (category.title == original) stringResource(resId) else category.title
}
